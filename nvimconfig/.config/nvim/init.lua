-- Minimal Neovim config for learning Vim

-- Line numbers
vim.opt.number = true
vim.opt.relativenumber = true

-- Indentation
vim.opt.tabstop = 2
vim.opt.shiftwidth = 2
vim.opt.expandtab = true

-- Better search
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- Keep cursor centered
vim.opt.scrolloff = 8

-- Enable mouse (optional)
vim.opt.mouse = "a"
